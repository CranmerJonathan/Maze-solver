public class Node {
	//declaring variables
	private int x;
	private int y;
	
	//Node Constructor
	public Node (int y, int x) {
		this.x = x;
		this.y = y;
	}
	
	//X getter method
	public int getX() {
		return x;
	}
	
	//Y getter method
	public int getY() {
		return y;
	}
}